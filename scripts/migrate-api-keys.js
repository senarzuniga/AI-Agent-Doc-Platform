#!/usr/bin/env node
/* eslint-disable no-console */
const fs = require('fs');
const path = require('path');
const readline = require('readline');
const { execSync } = require('child_process');

const { parseEnv, validateKeyMap } = require('./validate-api-keys');

const KEY_ORDER = [
  'OPENAI_API_KEY',
  'ANTHROPIC_API_KEY',
  'GROQ_API_KEY',
  'COHERE_API_KEY',
  'PINECONE_API_KEY',
  'QDRANT_API_KEY',
  'SUPABASE_URL',
  'SUPABASE_KEY',
  'AZURE_OPENAI_KEY',
  'AZURE_OPENAI_ENDPOINT',
];

const DEFAULT_SOURCES = [
  'C:/Users/Inaki Senar/Documents/GitHub/adaptive-sales-engine/.env',
  'C:/Users/Inaki Senar/Documents/GitHub/AI-FACTORY-v2/.env',
];

function maskValue(value) {
  if (!value) return '***';
  if (value.length <= 8) return `${value.slice(0, 2)}...${value.slice(-2)}`;
  return `${value.slice(0, 4)}...${value.slice(-4)}`;
}

function parseArgs(argv) {
  const args = { source: [], output: '.env', prefer: 'first' };
  for (let i = 2; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === '--source') {
      args.source.push(argv[i + 1]);
      i += 1;
    } else if (token === '--output') {
      args.output = argv[i + 1];
      i += 1;
    } else if (token === '--prefer') {
      args.prefer = argv[i + 1];
      i += 1;
    }
  }
  if (args.source.length === 0) {
    args.source = [...DEFAULT_SOURCES];
  }
  return args;
}

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function writeTemplateFiles(projectRoot) {
  const header = '# Generated by scripts/migrate-api-keys.js';
  const template = [header, ...KEY_ORDER.map((k) => `${k}=your_key_here`), ''].join('\n');
  fs.writeFileSync(path.join(projectRoot, '.env.template'), template, 'utf8');
  fs.writeFileSync(path.join(projectRoot, '.env.example'), template, 'utf8');
}

function parseEnvFileIfExists(envPath) {
  if (!fs.existsSync(envPath)) return {};
  return parseEnv(fs.readFileSync(envPath, 'utf8'));
}

function promptUser(question) {
  return new Promise((resolve) => {
    const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
    rl.question(question, (answer) => {
      rl.close();
      resolve(answer.trim());
    });
  });
}

async function resolveConflict(key, left, right, leftLabel, rightLabel, prefer) {
  console.log(`[CONFLICT] ${key}`);
  console.log(`  1) ${leftLabel}: ${maskValue(left)}`);
  console.log(`  2) ${rightLabel}: ${maskValue(right)}`);

  if (prefer === 'second') return right;
  if (!process.stdin.isTTY || prefer === 'first') return left;

  const answer = await promptUser('Choose value [1/2] (default 1): ');
  return answer === '2' ? right : left;
}

function backupSourceEnv(projectRoot, sourcePath) {
  const resolved = path.resolve(sourcePath);
  if (!fs.existsSync(resolved)) return;

  const fileName = path.basename(path.dirname(resolved)) || 'repo';
  const stamp = new Date().toISOString().slice(0, 10).replace(/-/g, '');
  const out = path.join(projectRoot, 'backups', `keys-backup-${fileName}-${stamp}.enc`);

  const cmd = `node scripts/encrypt-keys.js --source "${resolved}" --output "${out}"`;
  try {
    execSync(cmd, { stdio: 'inherit', cwd: projectRoot });
  } catch (_err) {
    console.warn(`[WARN] Backup encryption failed for ${resolved}. Set KEYS_ENCRYPTION_PASSWORD to enable.`);
  }
}

async function run() {
  const args = parseArgs(process.argv);
  const projectRoot = process.cwd();
  ensureDir(path.join(projectRoot, 'backups'));

  const sources = args.source.map((s) => path.resolve(s));
  const parsedBySource = sources.map((src) => ({ src, env: parseEnvFileIfExists(src) }));

  for (const item of parsedBySource) {
    backupSourceEnv(projectRoot, item.src);
  }

  const merged = {};
  const seenByKey = {};

  for (const item of parsedBySource) {
    const label = item.src;
    for (const [key, value] of Object.entries(item.env)) {
      if (!value) continue;
      if (!seenByKey[key]) {
        seenByKey[key] = { value, label };
        merged[key] = value;
        continue;
      }
      if (seenByKey[key].value === value) continue;
      merged[key] = await resolveConflict(
        key,
        seenByKey[key].value,
        value,
        seenByKey[key].label,
        label,
        args.prefer,
      );
    }
  }

  writeTemplateFiles(projectRoot);

  const ordered = [];
  for (const key of KEY_ORDER) {
    if (merged[key]) ordered.push(`${key}=${merged[key]}`);
  }
  for (const key of Object.keys(merged).sort()) {
    if (!KEY_ORDER.includes(key)) ordered.push(`${key}=${merged[key]}`);
  }

  const outPath = path.resolve(args.output);
  fs.writeFileSync(outPath, `${ordered.join('\n')}\n`, 'utf8');
  console.log(`[OK] Unified env written to ${outPath}`);

  const keyMap = {
    openai: merged.OPENAI_API_KEY,
    anthropic: merged.ANTHROPIC_API_KEY,
    groq: merged.GROQ_API_KEY,
    cohere: merged.COHERE_API_KEY,
    pinecone: merged.PINECONE_API_KEY,
    qdrant: merged.QDRANT_API_KEY,
    supabase: merged.SUPABASE_KEY,
    azure_openai: merged.AZURE_OPENAI_KEY,
  };

  const results = await validateKeyMap(keyMap);
  for (const row of results) {
    const status = row.present ? (row.valid ? 'VALID' : 'UNVERIFIED') : 'MISSING';
    console.log(`[CHECK] ${row.service}: ${status} (${row.masked})`);
  }
}

if (require.main === module) {
  run().catch((err) => {
    console.error('[ERROR] Migration failed:', err.message);
    process.exit(1);
  });
}

module.exports = {
  parseArgs,
  run,
};
